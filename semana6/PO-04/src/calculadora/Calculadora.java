package calculadora;

public class Calculadora {

    public static int soma(int num1, int num2){

        int soma;
        soma = num1 + num2;

        return soma;
    }

    public static float soma(float num1, float num2){

        float soma;
        soma = num1 + num2;

        return soma;
    }

    public static int subtracao(int num1, int num2){

        int subtracao;
        subtracao = num1 - num2;

        return subtracao;
    }

    public static float subtracao(float num1, float num2){

        float subtracao;
        subtracao = num1 - num2;

        return subtracao;
    }

    public static int multiplicacao(int num1, int num2){

        int multiplicacao;
        multiplicacao = num1 * num2;

        return multiplicacao;
    }

    public static float multiplicacao(float num1, float num2){

        float multiplicacao;
        multiplicacao = num1 * num2;

        return multiplicacao;
    }

    public static int divisao(int num1, int num2){

        int divisao;
        divisao = num1 / num2;

        return divisao;
    }

    public static float divisao(float num1, float num2){

        float divisao;
        divisao = num1 / num2;

        return divisao;
    }

}
